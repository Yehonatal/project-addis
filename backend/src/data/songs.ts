export class Song implements ISong {
    id: string;
    title: string;
    artist: string;
    album: string;
    year: number;
    genre: string;
    duration: string;
    createdAt: string;
    updatedAt: string;

    constructor({
        title,
        artist,
        album,
        year,
        genre,
        duration,
    }: Omit<ISong, "id" | "createdAt" | "updatedAt">) {
        this.id = uuidv4();
        this.title = title;
        this.artist = artist;
        this.album = album;
        this.year = year;
        this.genre = genre;
        this.duration = duration;
        this.createdAt = new Date().toISOString();
        this.updatedAt = new Date().toISOString();
    }
}

